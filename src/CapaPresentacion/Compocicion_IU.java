    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int filaSeleccionada = tabla_reporte_compocicion.getSelectedRow();
        if (filaSeleccionada > -1) {
            int aviso = JOptionPane.showConfirmDialog(rootPane, "Estas seguro de eliminar");
            if (aviso == 0) {
                int idComposicion = Integer.parseInt(tabla_reporte_compocicion.getValueAt(filaSeleccionada, 0).toString());
                CompocicionBD oComposicionBD = new CompocicionBD();
                boolean rpta = oComposicionBD.eliminarComposicion(idComposicion);
                if (rpta) {
                    exito("Se elimino con exicto");
                    reportar();
                    Limpiar();
                    txtSerie.requestFocus();

                } else {
                    error("Tienes problemas al eliminar");
                }
            }
        } else {
            advertencia("Tiene que seleccionar una composicion para eliminar");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

