    public void reportar() {
        //     setCursor(new java.awt.Cursor(java.awt.Cursor.WAIT_CURSOR));

        try {
            Limpiar_tabla_formulario();

            DefaultTableModel tabla_temporal_compocicion = (DefaultTableModel) this.tabla_reporte_compocicion.getModel();
            CompocicionBD oCompocicionBD = new CompocicionBD();

            List<Compocicion> lista_compociciones = oCompocicionBD.reportarcompocicion();

            int cant = lista_compociciones.size();

            for (int i = 0; i < cant; i++) {
                int idcompocicion = lista_compociciones.get(i).getIdcomposicion();
                String nombre_compocicion = lista_compociciones.get(i).getCoNombre();
                String serie_producto = lista_compociciones.get(i).getpSerie();

                Object[] data = {idcompocicion, nombre_compocicion, serie_producto};

                tabla_temporal_compocicion.addRow(data);
                // setCursor(new java.awt.Cursor(java.awt.Cursor.DEFAULT_CURSOR));
            }
            tabla_reporte_compocicion.setModel(tabla_temporal_compocicion);

        } catch (Exception e) {

        }
    }

    private void Limpiar_tabla_formulario() {
        DefaultTableModel tabla_temporal_productos = (DefaultTableModel) tabla_reporte_compocicion.getModel();
        tabla_temporal_productos.setRowCount(0);
    }

